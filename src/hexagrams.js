/**
 * 6개 효(lines)로부터 괘 번호를 구한다.
 * @param {number[]} bits - 6개 요소, 각 0(yin) 또는 1(yang), index 0=bottom
 * @returns {number} King Wen 괘 번호 (1~64)
 */
export function getHexagramNumber(bits) {
  const lowerKey = `${bits[0]}${bits[1]}${bits[2]}`;
  const upperKey = `${bits[3]}${bits[4]}${bits[5]}`;
  const lowerTri = BIT_TO_TRIGRAM[lowerKey];
  const upperTri = BIT_TO_TRIGRAM[upperKey];
  return KING_WEN[upperTri][lowerTri];
}

/**
 * 괘 번호 → 6개 효 비트 역산
 * @param {number} num - 1~64
 * @returns {number[]} [line1..line6], 1=yang, 0=yin (bottom to top)
 */
export function getHexagramBits(num) {
  // KING_WEN 역탐색: 번호 → (upper, lower) trigram
  for (const upper of Object.keys(KING_WEN)) {
    for (const lower of Object.keys(KING_WEN[upper])) {
      if (KING_WEN[upper][lower] === num) {
        // trigram name → bits
        const TRIGRAM_BITS = {
          '건': [1,1,1], '곤': [0,0,0], '진': [1,0,0], '감': [0,1,0],
          '간': [0,0,1], '손': [0,1,1], '이': [1,0,1], '태': [1,1,0],
        };
        return [...TRIGRAM_BITS[lower], ...TRIGRAM_BITS[upper]];
      }
    }
  }
  return [0,0,0,0,0,0];
}

/**
 * 괘 번호로 데이터를 가져온다.
 * @param {number} num - 1~64
 */
export function getHexagramData(num) {
  const d = HEXAGRAMS[num];
  if (!d) return null;
  return {
    number: num,
    name: d.n,
    hanja: d.h,
    fullName: d.f,
    keyword: d.k,
    symbol: d.s,
    judgment: d.j,
    image: d.i,
  };
}
